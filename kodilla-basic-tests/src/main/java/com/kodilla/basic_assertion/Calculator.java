package com.kodilla.basic_assertion;

public class Calculator {
    private int a;
    private int b;

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int square(int a) {
        return a * a;
    }
    public double square2(double c) {
        return c * c;
    }

}
